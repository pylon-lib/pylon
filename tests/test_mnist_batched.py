import torch
import torch.nn as nn
import torch.nn.functional as F
import torch.optim as optim

from pytorch_constraints.constraint import constraint
from pytorch_constraints.tnorm_solver import *
from pytorch_constraints.sampling_solver import WeightedSamplingSolver
from pytorch_constraints.circuit_solver import SemanticLossCircuitSolver


def get_solvers(num_samples):
    return [WeightedSamplingSolver(num_samples)]


class Net(nn.Module):
    def __init__(self):
        super(Net, self).__init__()
        self.conv1 = nn.Conv2d(1, 32, 3, 1)
        self.conv2 = nn.Conv2d(32, 64, 3, 1)
        self.dropout1 = nn.Dropout2d(0.25)
        self.dropout2 = nn.Dropout2d(0.5)
        self.fc1 = nn.Linear(9216, 128)
        self.fc2 = nn.Linear(128, 20)

    def forward(self, x):
        x = self.conv1(x)
        x = F.relu(x)
        x = self.conv2(x)
        x = F.relu(x)
        x = F.max_pool2d(x, 2)
        x = self.dropout1(x)
        x = torch.flatten(x, 1)
        x = self.fc1(x)
        x = F.relu(x)
        x = self.dropout2(x)
        x = self.fc2(x)

        return x.reshape(-1, 10, 2)


def train(constraint=None, epoch=100):

    net = Net()
    X, y = get_mnist_data()
    X = torch.cat((X, X, X))
    y = torch.cat((y, y, y))
    optimizer = optim.Adadelta(net.parameters(), lr=1.0)

    for i in range(epoch):

        optimizer.zero_grad()

        output = net(X)
        loss = F.cross_entropy(output[:, :, 1], y)

        if constraint:
            loss += constraint(output)

        loss.backward()
        optimizer.step()

    return net


def only_one(x):
    return torch.sum(x, 1) == 1


def test_only_one_mnist():

    X, y = get_mnist_data()
    X = torch.cat((X, X, X))
    y = torch.cat((y, y, y))

    for solver in get_solvers(num_samples=200):
        only_one_constraint = constraint(only_one, solver)
        net = train(only_one_constraint)

        assert(torch.argmax(torch.softmax(net(X), dim=-1), dim=-1).sum().item() == 3)


